class UserObj {
  String uid;
  String firstName;
  String image;
  String lastName;
  String phoneNumber;
  String status;
  String email;

  UserObj(String uid, String firstName, String image, String lastName,
      String phoneNumber, String status, String email) {
    this.uid = uid;
    this.firstName = firstName;
    this.image = image;
    this.lastName = lastName;
    this.phoneNumber = phoneNumber;
    this.status = status;
    this.email = email;
  }
}
